package com.mines.CurvaABCZ.Model;

public class CurvaCapitalEmpregadoInput {

    private Integer id;
    private Double valorCapital;
    private Double frequencia;
    private Integer entidadeGestora;

    public Integer getId() {
        return id;
    }
    public void setId(Integer id) {
        this.id = id;
    }
    public Double getValorCapital() {
        return valorCapital;
    }
    public void setValorCapital(Double valorCapital) {
        this.valorCapital = valorCapital;
    }
    public Double getFrequencia() {
        return frequencia;
    }
    public void setFrequencia(Double frequencia) {
        this.frequencia = frequencia;
    }
    public Integer getEntidadeGestora() {
        return entidadeGestora;
    }
    public void setEntidadeGestora(Integer entidadeGestora) {
        this.entidadeGestora = entidadeGestora;
    }
    
}
